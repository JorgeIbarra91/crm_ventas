-- Create regions table
CREATE TABLE public.regions (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE
);

-- Create comunas table
CREATE TABLE public.comunas (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    region_id INT REFERENCES public.regions(id) ON DELETE CASCADE
);

-- Create profiles table
CREATE TYPE public.user_role AS ENUM ('admin', 'manager', 'rep');

CREATE TABLE public.profiles (
    id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
    full_name TEXT,
    role public.user_role DEFAULT 'rep' NOT NULL
);

-- Create clients table
CREATE TABLE public.clients (
    rut TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    email TEXT UNIQUE,
    phone TEXT,
    address TEXT,
    comuna_id INT REFERENCES public.comunas(id) ON DELETE SET NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    created_by UUID REFERENCES public.profiles(id) ON DELETE SET NULL
);

-- Create sales table
CREATE TABLE public.sales (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    amount NUMERIC NOT NULL,
    sale_date TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    client_rut TEXT REFERENCES public.clients(rut) ON DELETE CASCADE,
    rep_id UUID REFERENCES public.profiles(id) ON DELETE SET NULL,
    notes TEXT
);

