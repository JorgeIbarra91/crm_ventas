-- Create client_status view
CREATE OR REPLACE VIEW public.client_status SECURITY INVOKER AS
SELECT
    c.rut,
    c.name AS client_name,
    c.email,
    c.phone,
    c.address,
    co.name AS comuna_name,
    r.name AS region_name,
    c.created_at,
    (SELECT MAX(s.sale_date) FROM public.sales s WHERE s.client_rut = c.rut) AS last_sale_date,
    CASE
        WHEN (SELECT MAX(s.sale_date) FROM public.sales s WHERE s.client_rut = c.rut) IS NULL THEN 'Red' -- No sales yet
        ELSE
            EXTRACT(DAY FROM (CURRENT_TIMESTAMP - (SELECT MAX(s.sale_date) FROM public.sales s WHERE s.client_rut = c.rut)))
    END AS days_since_last_sale_numerical,
    CASE
        WHEN (SELECT MAX(s.sale_date) FROM public.sales s WHERE s.client_rut = c.rut) IS NULL THEN 'Red'
        WHEN CURRENT_TIMESTAMP - (SELECT MAX(s.sale_date) FROM public.sales s WHERE s.client_rut = c.rut) <= INTERVAL '30 days' THEN 'Green'
        WHEN CURRENT_TIMESTAMP - (SELECT MAX(s.sale_date) FROM public.sales s WHERE s.client_rut = c.rut) <= INTERVAL '60 days' THEN 'Yellow'
        ELSE 'Red'
    END AS status_color
FROM
    public.clients c
LEFT JOIN
    public.comunas co ON c.comuna_id = co.id
LEFT JOIN
    public.regions r ON co.region_id = r.id;